// jsDelivr CDN mirrors the PokeAPI/sprites repo sin rate limits (GitHub Raw tiene límite ~60/h por IP)
const _SPRITE_CDN = 'https://cdn.jsdelivr.net/gh/PokeAPI/sprites@master';
const _SPRITE_RAW = 'https://raw.githubusercontent.com/PokeAPI/sprites/master';

function toCdnSprite(url) {
  if (!url) return url;
  return typeof url === 'string' ? url.replace(_SPRITE_RAW, _SPRITE_CDN) : url;
}

function getDexSpriteUrl(id) {
  return `${_SPRITE_CDN}/sprites/pokemon/${id}.png`;
}

// Carga los stats base desde PokeAPI o desde POKEMON_STATS local
async function getDexEntry(entry) {
  try {
    const data = await PokeAPI.getPokemon(entry.name);
    return {
      ...entry,
      spriteUrl: data.sprites.front_default ?? getDexSpriteUrl(entry.id),
      stats: {
        hp: data.stats.find(s => s.stat.name === 'hp').base_stat,
        atk: data.stats.find(s => s.stat.name === 'attack').base_stat,
        def: data.stats.find(s => s.stat.name === 'defense').base_stat,
        spa: data.stats.find(s => s.stat.name === 'special-attack').base_stat,
        spd: data.stats.find(s => s.stat.name === 'special-defense').base_stat,
        spe: data.stats.find(s => s.stat.name === 'speed').base_stat,
      },
    };
  } catch {
    const local = POKEMON_STATS[entry.name];
    return {
      ...entry,
      spriteUrl: getDexSpriteUrl(entry.id),
      stats: local ? { hp: local.hp, atk: local.atk, def: local.def, spa: local.spa, spd: local.spd, spe: local.spe } : null,
    };
  }
}
